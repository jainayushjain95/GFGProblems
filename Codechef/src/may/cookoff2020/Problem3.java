package may.cookoff2020;

import java.util.Scanner;

public class Problem3 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		StringBuilder str = new StringBuilder();
		while(t != 0) {
			str.append(solve() + "\n");
			t--;
		}
	}

	public static String solve() {
		StringBuilder str = new StringBuilder();
		return str.toString();
	}

}
